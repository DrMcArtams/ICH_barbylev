x = 10  # 1010 в двоичной системе
y = 5   # 0101 в двоичной системе

print(x & y)  # Выводит 0
print(x | y)  # Выводит 15
print(x ^ y)  # Выводит 15
print(x << 1)  # Выводит 20
print(x >> 1)  # Выводит 5
